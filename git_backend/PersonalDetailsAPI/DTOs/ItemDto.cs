// DTOs/ItemDto.cs
public class ItemDto
{
    public int Id { get; set; }           // Unique identifier
    public string FirstName { get; set; }  // Person's first name
    public string LastName { get; set; }   // Person's last name
    public string Email { get; set; }      // Person's email address
    public string Phone { get; set; }      // Person's phone number
    public string Address { get; set; }    // Person's street address
    public string City { get; set; }       // City
    public string State { get; set; }      // State or province
    public string PostalCode { get; set; } // Postal or ZIP code
}
